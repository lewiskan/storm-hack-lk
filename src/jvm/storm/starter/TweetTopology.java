import java.util.Map;

/**
 * This is a basic example of a Storm topology.
 */
public class TweetTopology {

  public static class StdoutBolt extends BaseRichBolt {
    OutputCollector _collector;

    @Override
    public void prepare(Map conf, TopologyContext context, OutputCollector collector) {
      _collector = collector;
    }

    @Override
    public void execute(Tuple tuple) {
      _collector.emit(tuple, new Values(tuple.getValue(0)));
      _collector.ack(tuple);
    }

    @Override
    public void declareOutputFields(OutputFieldsDeclarer declarer) {
      declarer.declare(new Fields("word"));
    }
  }

  public static void main(String[] args) throws Exception {
    TopologyBuilder builder = new TopologyBuilder();

    builder.setSpout("word", 
           //new TwitterSampleSpout("[Your customer key]",
           //     "[Your secret key]",
           //    "[Your access token]", 
           //    "[Your access secret]"), 1);
               new TwitterSampleSpout(
                "1sh8vxoGt0PffqUfEaAvXwLb7",
                 "TUGNy6K7NpXieEt8x6coKBJEYa8MTyTWVDJqwNRR3vWI22ZlgO",
                 "2516182968-SEWJ7w7vsVrGYIE4ea1wiXwdJ8ExuySmFcKsbTn", 
                 "YhxRWjzKzlXNvlkFyC4561WFkSbTWjbRaO1o3oVTaklIj"), 
               1);

    builder.setBolt("stdout", new StdoutBolt(), 3).shuffleGrouping("word");

    Config conf = new Config();
    conf.setDebug(true);

    if (args != null && args.length > 0) {
      conf.setNumWorkers(3);

      StormSubmitter.submitTopology(args[0], conf, builder.createTopology());
    }
    else {

      LocalCluster cluster = new LocalCluster();
      cluster.submitTopology("test", conf, builder.createTopology());
      Utils.sleep(100000);
      cluster.killTopology("test");
      cluster.shutdown();
    }
  }
}
